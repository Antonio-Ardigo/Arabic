def log(msg: str) -> None:
    print(msg, flush=True)


def synth_gtts(text: str, out_path: Path, lang: str = "ar") -> tuple[bool, str]:
    try:
        from gtts import gTTS  # type: ignore
    except ImportError:
        return False, "gTTS not installed (pip install gTTS)"
    try:
        out_path.parent.mkdir(parents=True, exist_ok=True)
        gTTS(text=text, lang=lang, slow=False).save(str(out_path))
        # gTTS sometimes leaves 0-byte files on partial failure — guard.
        if out_path.exists() and out_path.stat().st_size == 0:
            out_path.unlink()
            return False, "gTTS wrote 0-byte file"
        return True, ""
    except Exception as exc:  # network / quota / unsupported chars
        # Remove any 0-byte remnant gTTS may have created before failing.
        try:
            if out_path.exists() and out_path.stat().st_size == 0:
                out_path.unlink()
        except OSError:
            pass
        return False, str(exc)


def synth_elevenlabs(text: str, out_path: Path, voice: str) -> tuple[bool, str]:
    api_key = os.environ.get("ELEVENLABS_API_KEY")
    if not api_key:
        return False, "ELEVENLABS_API_KEY not set"
    try:
        from elevenlabs.client import ElevenLabs  # type: ignore
    except ImportError:
        return False, "elevenlabs client not installed"
    try:
        client = ElevenLabs(api_key=api_key)
        audio_stream = client.text_to_speech.convert(
            voice_id=voice,
            model_id="eleven_multilingual_v2",
            text=text,
            output_format="mp3_44100_128",
        )
        out_path.parent.mkdir(parents=True, exist_ok=True)
        with out_path.open("wb") as f:
            for chunk in audio_stream:
                if chunk:
                    f.write(chunk)
        return True, ""
    except Exception as exc:
        return False, str(exc)


def generate_one(text: str, out_path: Path, provider: str, voice: str) -> tuple[bool, str]:
    if provider == "elevenlabs":
        ok, err = synth_elevenlabs(text, out_path, voice)
        if ok:
            return True, ""
        ok, gerr = synth_gtts(text, out_path)
        return ok, err if ok else f"{err}; gTTS fallback: {gerr}"
    return synth_gtts(text, out_path)


def emit_regen_script(deck_dir: Path) -> list[Path]:
    """Write standalone recovery scripts alongside the manifest.

    Emits three files so users on any OS have a one-click path:
      regen_audio.py   — cross-platform Python runner
      regen_audio.bat  — Windows one-click wrapper (installs gTTS, runs .py, opens deck.html)
      regen_audio.sh   — macOS/Linux one-click wrapper (same flow)
    """
    targets: list[Path] = []

    py_path = deck_dir / "regen_audio.py"
    py_path.write_text(REGEN_SCRIPT_TEMPLATE, encoding="utf-8")
    try:
        py_path.chmod(0o755)
    except OSError:
        pass
    targets.append(py_path)

    bat_path = deck_dir / "regen_audio.bat"
    # Windows line endings for .bat (cmd.exe is happier with CRLF)
    bat_path.write_text(REGEN_BAT_TEMPLATE.replace("\n", "\r\n"), encoding="utf-8")
    targets.append(bat_path)

    sh_path = deck_dir / "regen_audio.sh"
    sh_path.write_text(REGEN_SH_TEMPLATE, encoding="utf-8")
    try:
        sh_path.chmod(0o755)
    except OSError:
        pass
    targets.append(sh_path)

    return targets


def main() -> int:
    ap = argparse.ArgumentParser(description="Generate MP3 audio for an Arabic flashcard deck.")
    ap.add_argument("--manifest", required=True, help="Path to cards.json")
    ap.add_argument("--out", required=True, help="Output directory for mp3 files")
    ap.add_argument("--variety", choices=["najdi", "msa"], default="msa",
                    help="Deck variety (affects log messages only — synthesis always uses Arabic text)")
    ap.add_argument("--provider", choices=["gtts", "elevenlabs"], default="gtts")
    ap.add_argument("--voice", default="Rachel", help="Voice name/id for ElevenLabs")
    ap.add_argument("--verbose", action="store_true")
    ap.add_argument("--no-regen-script", action="store_true",
                    help="Don't write a standalone regen_audio.py on failure.")
    args = ap.parse_args()

    manifest_path = Path(args.manifest)
    out_dir = Path(args.out)
    out_dir.mkdir(parents=True, exist_ok=True)

    if not manifest_path.exists():
        log(f"[error] manifest not found: {manifest_path}")
        return 2

    manifest = json.loads(manifest_path.read_text(encoding="utf-8"))
    cards = manifest.get("cards", [])
    if not cards:
        log("[error] manifest has no cards.")
        return 2

    log(f"[info] variety={args.variety}  provider={args.provider}  cards={len(cards)}")
    if args.variety == "najdi" and args.provider == "gtts":
        log("[note] gTTS has no native Najdi voice — audio will approximate MSA pronunciation.")

    success = 0
    failures: list[tuple[str, str]] = []  # (filename, reason)
    expected = 0
    first_error_printed = False

    for card in cards:
        slug = card.get("slug")
        if not slug:
            log("[warn] card missing slug; skipping.")
            continue

        jobs = []
        if card.get("arabic"):
            jobs.append((card["arabic"], out_dir / f"{slug}.mp3"))
        for i, sent in enumerate(card.get("sentences", []), start=1):
            if sent.get("ar"):
                jobs.append((sent["ar"], out_dir / f"{slug}_ex{i}.mp3"))

        for text, path in jobs:
            expected += 1
            if args.verbose:
                log(f"[audio] {path.name}  ←  {text[:60]}")
            ok, err = generate_one(text, path, args.provider, args.voice)
            if ok:
                success += 1
            else:
                failures.append((path.name, err))
                if not first_error_printed:
                    log(f"[warn] first synthesis failure: {path.name}  ({err})")
                    first_error_printed = True

    # Summary + recovery plan
    log("")
    log(f"✓ {success}/{expected} files synthesised → {out_dir}")

    if failures:
        log(f"✗ {len(failures)} files failed (first error shown above).")
        if args.provider == "gtts" and any("google" in err.lower() or "connect" in err.lower() for _, err in failures):
            log("[hint] gTTS couldn't reach translate.google.com — common in sandboxed environments.")

        if not args.no_regen_script:
            deck_dir = manifest_path.parent
            regen_paths = emit_regen_script(deck_dir)
            log(f"[recovery] wrote standalone regen scripts in {deck_dir}:")
            for p in regen_paths:
                log(f"           - {p.name}")
            log(f"[recovery] on an unrestricted network:")
            log(f"             Windows : double-click regen_audio.bat")
            log(f"             macOS   : bash regen_audio.sh")
            log(f"             Linux   : bash regen_audio.sh")
            log(f"             Any OS  : pip install gTTS && python regen_audio.py")

    return 0 if not failures else 1


if __name__ == "__main__":
    sys.exit(main())
