#!/usr/bin/env python3
"""Generate MP3 audio for an Arabic flashcard deck.

Reads a cards.json manifest, synthesises audio for each headword and each
example sentence, and writes MP3 files to the output directory.

Providers:
    gtts        — default, free, no API key (pip install gTTS)
    elevenlabs  — higher quality, requires ELEVENLABS_API_KEY
                  (pip install elevenlabs)

Network-restricted environments:
    If gTTS cannot reach translate.google.com (common in sandboxed
    environments with allowlists), no silent placeholders are created.
    Instead, a standalone `regen_audio.py` is dropped next to the deck
    so the user can synthesise audio later on a machine with normal
    internet access. Override with --no-regen-script.
"""

from __future__ import annotations

import argparse
import json
import os
import sys
import textwrap
from pathlib import Path

REGEN_SCRIPT_TEMPLATE = r'''#!/usr/bin/env python3
"""Regenerate MP3 audio for this deck using gTTS.

Auto-generated by the arabic-flashcards plugin when sandbox synthesis failed.
Run on a machine with normal internet access.

Requirements:
    pip install gTTS

Usage:
    python regen_audio.py
"""
import json
import sys
from pathlib import Path

try:
    from gtts import gTTS
except ImportError:
    print("gTTS not installed. Run:  pip install gTTS")
    sys.exit(1)

HERE = Path(__file__).parent
MANIFEST = HERE / "cards.json"
AUDIO_DIR = HERE / "audio"
AUDIO_DIR.mkdir(exist_ok=True)

LANG = "ar"  # Modern Standard Arabic voice (closest match for Najdi too)


def synth(text, out):
    gTTS(text=text, lang=LANG, slow=False).save(str(out))


def main():
    data = json.loads(MANIFEST.read_text(encoding="utf-8"))
    cards = data["cards"]
    n = 0
    for c in cards:
        slug = c["slug"]
        synth(c["arabic"], AUDIO_DIR / f"{slug}.mp3"); n += 1
        for i, s in enumerate(c["sentences"], start=1):
            synth(s["ar"], AUDIO_DIR / f"{slug}_ex{i}.mp3"); n += 1
        print(f"  {c['arabic']}  ({slug})")
    print(f"\n[OK] {n} MP3 files in {AUDIO_DIR}")


if __name__ == "__main__":
    main()
'''

